// 아파트별 실거래 내역 생성 함수
function generateTradeHistory(aptId: string, basePrice: number, area: string): TradeHistory[] {
  const history: TradeHistory[] = [];
  const now = new Date();
  const variance = 0.15; // 가격 변동 범위 15%

  for (let i = 23; i >= 0; i--) {
    const date = new Date(now.getFullYear(), now.getMonth() - i, 1);
    const yearMonth = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}`;

    // 가격 트렌드: 전반적으로 상승하되 랜덤 변동
    const trendFactor = 1 + (23 - i) * 0.003;
    const randomFactor = 1 + (Math.random() - 0.5) * variance;
    const price = Math.round((basePrice * trendFactor * randomFactor) / 100) * 100;

    // 월당 1-3건 거래
    const tradeCount = Math.floor(Math.random() * 3) + 1;
    for (let j = 0; j < tradeCount; j++) {
      history.push({
        id: `trade-${aptId}-${yearMonth}-${j}`,
        apartmentId: aptId,
        dealDate: yearMonth,
        floor: Math.floor(Math.random() * 25) + 1,
        area,
        price,
      });
    }
  }

  return history;
}

// 면적별 기준가 비율 — 84㎡ 기준 배율
// 작은 면적은 저렴하고, 큰 면적은 비쌈 (비선형 비율)
const AREA_PRICE_RATIO: Record<string, number> = {
  '49': 0.58, '54': 0.63, '59': 0.72, '67': 0.80, '74': 0.88,
  '76': 0.90, '82': 0.97, '84': 1.00, '101': 1.18, '109': 1.28,
  '112': 1.31, '114': 1.35, '115': 1.37, '119': 1.40, '144': 1.68, '145': 1.70,
  '164': 1.93, '176': 2.06, '206': 2.40, '235': 2.73, '244': 2.83,
};

// 면적 기준 가격 계산 헬퍼
function getPriceByArea(basePrice84: number, area: string): number {
  const ratio = AREA_PRICE_RATIO[area] ?? 1.0;
  return Math.round((basePrice84 * ratio) / 100) * 100;
}

// 아파트별 모든 면적 거래 데이터 생성 헬퍼
function generateAllAreasHistory(
  aptId: string,
  basePrice84: number,
  areas: string[]
): TradeHistory[] {
  return areas.flatMap((area) =>
    generateTradeHistory(aptId, getPriceByArea(basePrice84, area), area)
  );
}

