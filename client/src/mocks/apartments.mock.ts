// 아파트별 실거래 내역 생성 함수
function generateTradeHistory(aptId: string, basePrice: number, area: string): TradeHistory[] {
  const history: TradeHistory[] = [];
  const now = new Date();
  const variance = 0.15; // 가격 변동 범위 15%

  for (let i = 23; i >= 0; i--) {
    const date = new Date(now.getFullYear(), now.getMonth() - i, 1);
    const yearMonth = `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}`;

    // 가격 트렌드: 전반적으로 상승하되 랜덤 변동
    const trendFactor = 1 + (23 - i) * 0.003;
    const randomFactor = 1 + (Math.random() - 0.5) * variance;
    const price = Math.round((basePrice * trendFactor * randomFactor) / 100) * 100;

    // 월당 1-3건 거래
    const tradeCount = Math.floor(Math.random() * 3) + 1;
    for (let j = 0; j < tradeCount; j++) {
      history.push({
        id: `trade-${aptId}-${yearMonth}-${j}`,
        apartmentId: aptId,
        date: yearMonth,
        floor: Math.floor(Math.random() * 25) + 1,
        area,
        price,
      });
    }
  }

  return history;
}

