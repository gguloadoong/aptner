// features 자동 해석 헬퍼 — features가 이미 있으면 그대로 반환
function resolveFeatures(
  name: string,
  totalUnits: number,
  builtYear: number,
  preset?: ComplexFeature[]
): ComplexFeature[] {
  if (preset) return preset;
  const f: ComplexFeature[] = [];
  if (BRAND_NAMES.some((b) => name.includes(b))) f.push('brand');
  if (totalUnits >= 1000) f.push('large');
  if (builtYear >= 2020) f.push('new');
  return f;
}

